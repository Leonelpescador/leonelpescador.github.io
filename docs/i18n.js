window.__applyLang = function(lang) {
  const dict = window.__i18n[lang] || window.__i18n['es'];
  document.querySelectorAll('[data-i18n]').forEach(function(el) {
    const key = el.getAttribute('data-i18n');
    if (dict[key] !== undefined) el.textContent = dict[key];
  });
  document.querySelectorAll('[data-i18n-html]').forEach(function(el) {
    const key = el.getAttribute('data-i18n-html');
    if (dict[key] !== undefined) el.innerHTML = dict[key];
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(function(el) {
    const key = el.getAttribute('data-i18n-placeholder');
    if (dict[key] !== undefined) el.placeholder = dict[key];
  });
  document.documentElement.lang = lang;
  window.__lang = lang;
};

window.__initLangModal = function() {
  const saved = localStorage.getItem('lang');
  if (saved) { window.__lang = saved; return; }

  const overlay = document.createElement('div');
  overlay.id = '__lang-modal';
  overlay.style.cssText = [
    'position:fixed',
    'inset:0',
    'z-index:99999',
    'background:#040904',
    'display:flex',
    'flex-direction:column',
    'align-items:center',
    'justify-content:center',
    'gap:40px',
    "font-family:'JetBrains Mono',monospace",
  ].join(';');

  overlay.innerHTML = [
    '<div style="color:#10b981;font-size:1.4rem;font-weight:700;letter-spacing:-1px;">&lt;JLP/&gt;</div>',
    '<div style="text-align:center;">',
      '<p style="color:#e8f5e9;font-size:0.7rem;letter-spacing:0.32em;margin-bottom:16px;">// select_language</p>',
      '<p style="color:#b8d4b8;font-size:0.65rem;letter-spacing:0.18em;">choose your preferred language</p>',
    '</div>',
    '<div style="display:flex;gap:20px;">',
      '<button data-lang="es" style="font-family:\'JetBrains Mono\',monospace;font-size:0.85rem;font-weight:700;padding:14px 32px;background:transparent;color:#10b981;border:1px solid rgba(16,185,129,0.5);border-radius:3px;cursor:pointer;letter-spacing:0.1em;transition:all 0.25s;">ES · Español</button>',
      '<button data-lang="en" style="font-family:\'JetBrains Mono\',monospace;font-size:0.85rem;font-weight:700;padding:14px 32px;background:transparent;color:#e8f5e9;border:1px solid rgba(232,245,233,0.25);border-radius:3px;cursor:pointer;letter-spacing:0.1em;transition:all 0.25s;">EN · English</button>',
    '</div>',
    '<div style="width:44px;height:1px;background:rgba(16,185,129,0.15);"></div>',
  ].join('');

  // Hover effects
  overlay.querySelectorAll('button').forEach(function(btn) {
    btn.addEventListener('mouseenter', function() {
      btn.style.borderColor = 'rgba(16,185,129,0.8)';
      btn.style.color = '#10b981';
    });
    btn.addEventListener('mouseleave', function() {
      const isEs = btn.getAttribute('data-lang') === 'es';
      btn.style.borderColor = isEs ? 'rgba(16,185,129,0.5)' : 'rgba(232,245,233,0.25)';
      btn.style.color = isEs ? '#10b981' : '#e8f5e9';
    });
    btn.addEventListener('click', function() {
      const lang = btn.getAttribute('data-lang');
      localStorage.setItem('lang', lang);
      window.__lang = lang;
      window.__applyLang(lang);
      overlay.style.opacity = '0';
      overlay.style.transition = 'opacity 0.4s ease';
      setTimeout(function() { overlay.remove(); }, 420);
    });
  });

  document.body.appendChild(overlay);
};

document.addEventListener('DOMContentLoaded', window.__initLangModal);
